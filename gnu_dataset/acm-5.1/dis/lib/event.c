#include <dis/dis.h>

dis_event_id *
DISxIssueEventID(DISxApplicationInfo * info, dis_event_id * event)
{
	event->sim_id = info->id;
	event->event_id = ++info->last_event;
	if (info->last_event == 0xffff) {
		info->last_event = 0;
	}
	return event;
}
